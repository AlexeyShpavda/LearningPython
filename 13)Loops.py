print("### While you're here ###")
count = 0

if count < 10:
  print("Hello, I am an if statement and count is", count)

while count < 10:
  print("Hello, I am a while and count is", count)
  count += 1

print("### Condition ###")
loop_condition = True

while loop_condition:
  print("I am a loop")
  loop_condition = False

print("### While you're at it ###")
num = 1

while num <= 10:
  print(num ** 2)
  num += 1